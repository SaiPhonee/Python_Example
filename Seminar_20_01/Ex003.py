# 3. По заданному номеру дня недели вывести его название
n = int(input('Введите число от 1 до 7 '))
if n == 1:
    print('Понедельник')
if n == 2:
    print('Вторник')
if n == 3:
    print('Среда')
if n == 4:
    print('Четверг')
if n == 5:
    print('Пятница')
if n == 6:
    print('Суббота')
if n == 7:
    print('Воскресенье')
